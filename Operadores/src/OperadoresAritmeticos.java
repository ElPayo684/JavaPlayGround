public class OperadoresAritmeticos {
    public static void main(String[] args) {
        
        int i = 5, j = 4;

        int summ = i + j;
        System.out.println("La suma de " + i + " + " + j + " es: " + summ);
        int resta = i - j;
        System.out.println("La resta de " + i + " - " + j + " es: " + resta);
        int mult = i * j;
        System.out.println("La multiplicación de " + i + " * " + j + " es: " + mult);
        float div = (float) i / j;
        System.out.println("La división de " + i + " / " + j + " es: " + div);
    }
}
